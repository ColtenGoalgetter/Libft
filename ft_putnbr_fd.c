#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	v;

	v = 0;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n *= -1;
		write(fd, "-", 1);
	}
	if (n > 9)
	{
		ft_putnbr_fd((n / 10), fd);
		v = (n % 10) + 48;
		write(fd, &v, 1);
	}
	else
	{
		v = n + 48;
		write(fd, &v, 1);
	}
}
